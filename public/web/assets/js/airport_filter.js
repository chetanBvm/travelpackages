    function getAirports(e,p) {
        
        p = p.toUpperCase();
        var r = $.grep(e, function(e, r) {
                return -1<e.NAME.toUpperCase().indexOf(p)||-1<e.CODE.toUpperCase().indexOf(p)||-1<e.CITY.toUpperCase().indexOf(p)||-1<e.COUNTRY.toUpperCase().indexOf(p);
        });
       
        if(r.length > 0){
            t = '<div class="q_result packRes">';
            $.each(r, function(e, p) {
                t += '<p class="dropResDepCity" data-code="' + p.CODE + '" data-city="'+p.CITY+'" data-country="'+p.COUNTRY+'">' + p.NAME + " (" + p.CODE + ")</p>"
            });
            t += "</div>";
        }else{
            t = "";
        }
        return t;
    }